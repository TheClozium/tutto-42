#include <unistd.h>

int	main(int argc, char **argv)
{
	int	k;
	int	l;

	k = argc - 1;
	while (k >= 1)
	{
		l = 0;
		while (argv[k][l] != '\0')
		{
			write (1, &argv[k][l], 1);
			l++;
		}
		write (1, "\n", 1);
		k--;
	}
	return (0);
}
