#include <unistd.h>

void ft_putchar(void);

void rush(int x, int y)
{
    int i;
    int j;

    i = -1;
    while (++i < y)
    {
        j = -1;
        while (++j < x)
        {
            if ((i == 0 || i == y - 1) && (j == 0 || j == x - 1))
                write (1, 'o', 1);
            else if (i == 0 || i == y - 1 || j == 0 || j == x - 1)
                write (1, '|', 1);
            else
                write (1, ' ', 1);
        }
        write (1, '\n', 1);
    }
}

