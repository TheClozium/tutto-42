#include <string.h>

int	ft_str_is_printable(char *str)
{
	int	k;

	k = 0;
	while (str[k] != '\0')
	{
		if (!(str[k] >= 32 && str[k] <= 126))
		{
			return (0);
		}
		k++;
	}
	return (1);
}
