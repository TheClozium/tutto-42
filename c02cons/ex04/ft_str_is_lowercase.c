#include <string.h>

int	ft_str_is_lowercase(char *str)
{
	int	j;

	j = 0;
	while (str[j] != '\0')
	{
		if (!(str[j] >= 'a' && str[j] <= 'z'))
		{
			return (0);
		}
		j++;
	}
	return (1);
}
