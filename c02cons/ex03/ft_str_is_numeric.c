#include <string.h>

int	ft_str_is_numeric(char *str)

{
	int	f;

	f = 0;
	while (str[f] >= '0' && str[f] <= '9')
	{
		f++;
	}
	if (str[f] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
