#include <string.h>

int	ft_str_is_alpha(char *str)
{
	int	k;

	k = -1;
	while (str[++k] != '\0')
	{
		if (!((str[k] >= 'a' && str[k] <= 'z')
				|| (str[k] >= 'A' && str[k] <= 'Z')))
			return (0);
	}
	return (1);
}
