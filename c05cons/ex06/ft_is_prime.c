int	ft_is_prime(int nb)
{
	int	k;

	k = 2;
	if (nb <= 1)
	{
		return (0);
	}
	while (k <= (nb / 2))
	{
		if (nb % k == 0)
		{
			return (0);
		}
		k++;
	}
	return (1);
}
